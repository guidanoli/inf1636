package gui;

/**
 * 
 * <p>The <code>AreaMouseListener</code> interface is suited for
 * mouse events occurring inside a determined area on screen. The
 * methods to be implemented follow:
 * 
 * <ul>
 * <li>The {@link AreaMouseListener#action() action()} function
 * is triggered whenever the user left clicks on the area.</li>
 * </ul>
 * 
 * @author guidanoli
 *
 */
public interface AreaMouseListener {
	public void action();
}
